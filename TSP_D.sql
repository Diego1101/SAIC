
CREATE PROCEDURE TSP_AGREGARSOLICITUD
(
	@ID_SERV	INT,
	@PIEZA		NVARCHAR(100),
	@DESC		NVARCHAR(200)
)
AS
BEGIN

	IF EXISTS(SELECT SR_ID ID FROM SERVICIO WHERE SR_ID=@ID_SERV)
	BEGIN
		
		INSERT INTO SOLICITUD VALUES(@ID_SERV, 0, @PIEZA, 1, @DESC) 
		SELECT '1' --AGREGADA
	END
	ELSE SELECT '0' -- NO EXIXSTE EL SERVICIO
END
GO